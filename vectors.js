class Vector2 {
    /**
     * @param {number} x 
     * @param {number} z 
     */
    constructor(x, z) {
        this.x = x;
        this.y = z;
    }
}

class Vector3 {

    /**
     * @param {number} x 
     * @param {number} y 
     * @param {number} z 
     */
    constructor(x, y, z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
